            string playAgain = "yes";
            //while loop to make 
            while (playAgain.ToLower() == "yes" && !trueORfalse)
            {
                int attempts = 5;
                int number = RandomNumber();

                //for loop to declear how many attempts the user have and how many is used 
                for (int tries = 1; tries <= attempts; tries++)
                {
                    Console.WriteLine($"Attempt {tries}: ");
                    int user = Convert.ToInt32(Console.ReadLine());

                    //if else statements, inside the for loop, to declear if the user is close or not to finding the random number
                    if (user < number)
                    {
                        Console.WriteLine("The number you guessed is to low, try again.\n");
                    }
                    else if (user > number)
                    {
                        Console.WriteLine("The number you guessed is to high, guess again.\n");
                    }
                    else 
                    {
                        Console.ForegroundColor = ConsoleColor.DarkYellow;
                        Console.WriteLine("Wihooo, you did it. ＼(＾O＾)／ \n");
                        Console.ResetColor();
                        trueORfalse = true;
                        break;
                    }
                    //to show what the actual number was
                    if (tries == attempts)
                    {
                        Console.WriteLine($"Sorry, you have used upp all your {attempts} attempts.");
                        Console.WriteLine($"The answer was {number}\n");
                        break;
                    }
                }
                //makes the game either continue or stop, depends on what the user choose 
                Console.WriteLine("Do you want to play again? \nYes or No");
                playAgain = Console.ReadLine().ToLower();
            }
